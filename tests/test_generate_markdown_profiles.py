class TestYamlEscaping:
    """YAML frontmatter values must handle double quotes safely."""

    def test_yaml_escape_plain_string(self):
        assert _yaml_escape("hello") == '"hello"'

    def test_yaml_escape_string_with_double_quotes(self):
        """Double quotes inside must be escaped to prevent YAML parse errors."""
        result = _yaml_escape('He said "hello"')
        assert result == '"He said \\"hello\\""'

    def test_yaml_escape_string_with_colon(self):
        """Colons in YAML values are dangerous without quoting."""
        result = _yaml_escape("Emporia: Kansas")
        assert result == '"Emporia: Kansas"'

    def test_yaml_escape_integer(self):
        """Integers should NOT be quoted."""
        assert _yaml_escape(1) == "1"
        assert _yaml_escape(93) == "93"

    def test_yaml_escape_float(self):
        """Floats should NOT be quoted."""
        assert _yaml_escape(3.14) == "3.14"

    def test_yaml_escape_zero(self):
        """Zero should NOT be quoted."""
        assert _yaml_escape(0) == "0"

    def test_yaml_escape_backslash(self):
        """Backslashes must be escaped before quotes."""
        result = _yaml_escape("path\\to\\file")
        assert result == '"path\\\\to\\\\file"'

    def test_yaml_escape_empty_string(self):
        assert _yaml_escape("") == '""'

    def test_frontmatter_survives_quotes_in_name(self):
        """A race name containing quotes should produce valid YAML frontmatter."""
        entry = {"slug": "test", "tier": 1, "overall_score": 50}
        rd = {
            "name": 'The "Epic" Race',
            "vitals": {"location": "Here"},
            "gravel_god_rating": {"tier": 1, "overall_score": 50, "discipline": "gravel"},
        }
        fm = _frontmatter("test", entry, rd)
        assert "---" in fm
        assert '\\"Epic\\"' in fm
        # Should not have unescaped double quotes inside a YAML value
        lines = fm.splitlines()
        for line in lines:
            if line.startswith("name:"):
                # The value should be properly quoted
                val = line.split(":", 1)[1].strip()
                assert val.startswith('"') and val.endswith('"')


