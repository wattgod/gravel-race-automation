class TestMarkdownPipeEscaping:
    """Pipe characters in table cell values must be escaped."""

    def test_md_escape_pipe(self):
        assert _md_escape("A | B") == "A \\| B"

    def test_md_escape_newline(self):
        assert _md_escape("line1\nline2") == "line1 line2"

    def test_md_escape_none(self):
        assert _md_escape(None) == ""

    def test_md_escape_normal(self):
        assert _md_escape("Emporia, Kansas") == "Emporia, Kansas"

    def test_pipe_in_real_profiles_doesnt_break_tables(self, index):
        """Scan some profiles for broken table rows."""
        errors = []
        for r in index[:20]:
            md = generate_profile(r["slug"], r, RACE_DATA_DIR)
            if md is None:
                continue
            for i, line in enumerate(md.splitlines(), 1):
                if line.startswith("|"):
                    # Replace escaped pipes before counting
                    cleaned = line.replace("\\|", "XX")
                    cols = cleaned.count("|")
                    # A valid table row should have at least 3 pipes (| a | b |)
                    if cols < 3:
                        errors.append(f"{r['slug']} line {i}: only {cols} pipes: {line[:80]}")
        assert not errors, f"Broken table rows: {errors}"


