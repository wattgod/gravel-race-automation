class TestEdgeCases:
    def test_race_with_string_terrain(self, index_map):
        """Some races have terrain as a plain string instead of dict."""
        # Find a race with string terrain
        for slug in index_map:
            f = RACE_DATA_DIR / f"{slug}.json"
            if f.exists():
                data = json.loads(f.read_text())
                rd = data.get("race", data)
                if isinstance(rd.get("terrain"), str):
                    md = generate_profile(slug, index_map[slug], RACE_DATA_DIR)
                    assert md is not None
                    assert "## Terrain" in md
                    return
        pytest.skip("No race with string terrain found")

    def test_race_with_rider_intel(self, index_map):
        """Races with rider_intel should have Riders Report section."""
        for slug in index_map:
            f = RACE_DATA_DIR / f"{slug}.json"
            if f.exists():
                data = json.loads(f.read_text())
                rd = data.get("race", data)
                if rd.get("youtube_data", {}).get("rider_intel"):
                    md = generate_profile(slug, index_map[slug], RACE_DATA_DIR)
                    assert md is not None
                    assert "## Riders Report" in md
                    return
        pytest.skip("No race with rider_intel found")

    def test_missing_sections_gracefully_omitted(self, index_map):
        """A race missing some fields should still generate without errors."""
        # All 328 should generate without errors
        errors = []
        for slug in list(index_map.keys())[:50]:
            try:
                md = generate_profile(slug, index_map[slug], RACE_DATA_DIR)
                assert md is not None
            except Exception as e:
                errors.append(f"{slug}: {e}")
        assert not errors, f"Errors in profile generation: {errors}"


