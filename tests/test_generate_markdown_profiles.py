class TestCitationDeduplication:
    """Citations with the same URL should be deduplicated."""

    def test_dedup_by_url(self):
        rd = {
            "citations": [
                {"url": "https://example.com/a", "label": "Source A", "category": "official"},
                {"url": "https://example.com/a", "label": "Source A dup", "category": "official"},
                {"url": "https://example.com/b", "label": "Source B", "category": "media"},
            ]
        }
        result = _section_citations(rd)
        assert result.count("example.com/a") == 1  # deduped
        assert "example.com/b" in result

    def test_no_citations_returns_empty(self):
        assert _section_citations({}) == ""
        assert _section_citations({"citations": []}) == ""

    def test_real_profile_no_duplicate_urls(self, index_map):
        """Check a real profile for citation URL uniqueness."""
        md = generate_profile("unbound-200", index_map["unbound-200"], RACE_DATA_DIR)
        assert md is not None
        # Count citation URLs in the Citations section
        in_citations = False
        urls = []
        for line in md.splitlines():
            if line.startswith("## Citations"):
                in_citations = True
                continue
            if in_citations and line.startswith("## "):
                break
            if in_citations and line.startswith("- ["):
                # Extract URL from [label](url)
                start = line.index("](") + 2
                end = line.index(")", start)
                urls.append(line[start:end])
        assert len(urls) == len(set(urls)), f"Duplicate citation URLs: {urls}"

    def test_citation_no_url_key(self):
        """Citation without a 'url' key should be silently skipped, not crash."""
        rd = {"citations": [{"label": "No URL", "category": "other"}]}
        result = _section_citations(rd)
        assert result == ""

    def test_citation_duplicate_label_different_url(self):
        """Same label but different URL should NOT be deduped."""
        rd = {
            "citations": [
                {"url": "https://a.com", "label": "Source", "category": "official"},
                {"url": "https://b.com", "label": "Source", "category": "media"},
            ]
        }
        result = _section_citations(rd)
        assert "a.com" in result
        assert "b.com" in result


